                chkRjm.Checked = listaFamilia[0].Rjm.Equals("Sim") ? true : false;
                chkCulto.Checked = listaFamilia[0].Culto.Equals("Sim") ? true : false;
                chkMeiaHora.Checked = listaFamilia[0].MeiaHora.Equals("Sim") ? true : false;
                chkOficial.Checked = listaFamilia[0].Oficial.Equals("Sim") ? true : false;
                chkTroca.Checked = listaFamilia[0].Troca.Equals("Sim") ? true : false;
            }
            catch (SqlException exl)
            {
                throw exl;
            }
            catch (Exception ex)
            {
                throw ex;
            }
        }

